#include "calculator.h"

int add(int a, int b)
{
	int result;

	result = a + b;
	return (result);
}

int sub(int a, int b)
{
	int result;

	result = a - b;
	return (result);
}

int div(int a, int b)
{
	int result;

	result = a / b;
	return (result);
}

int mul(int a, int b)
{
	int result;

	result = a * b;
	return (result);
}

int mod(int a, int b)
{
	int result;

	result = a % b;
	return (result);
}
